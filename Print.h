#include <stdio.h>

//--------------------------------------------------------------------------

void Print_int(int data){

	printf("%d", data);

	return;
}

void Print_double(double data){

	printf("%lg", data);

	return;
}

void Print_unsigned_int(unsigned int data){

	printf("%u", data);

	return;
}

void Print_long_long(long long data){

	printf("%lld", data);

	return;
}